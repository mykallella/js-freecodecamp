let seven = 7; // Number
let three = "3"; // String
console.log(seven + three); // Não somou, fez concatenação. 
console.log('Tipo do Dado (7): ' + typeof seven); 
console.log('Tipo do Dado ("3"): ' + typeof three);

console.log('\n---------------------------------------------------------\n')

// Exemplos
console.log('Tipo do Dado (""): ' + typeof "");
console.log('Tipo do Dado (0): ' + typeof 0);
console.log('Tipo do Dado ([]): ' + typeof []);
console.log('Tipo do Dado ({}): ' + typeof {});

