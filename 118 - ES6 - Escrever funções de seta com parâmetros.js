// Função de SETA () => (SIMPLIFICADA) COM PARÂMETRO
const doubler1 = (item) => item * 2; // Parâmetro: item
console.log(doubler1(4)); // Chamada 

// Função de SETA () => (SIMPLIFICADA) COM PARÂMETRO (SEM PARÊNTESES)
const doubler2 = item => item * 2; // Parâmetro: item
console.log(doubler2(4)); // Chamada


// Função de SETA () => (SIMPLIFICADA) COM PARÂMETRO (DOIS PARÂMETROS)
const multiplier = (item, multi) => item * multi; // Parâmetros: item e multi
console.log(multiplier(4, 2)); // Chamada


